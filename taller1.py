""" 1. Qué operadores utiliza Python en los siguientes casos:

A. División Modular
B. Exponenciación
C. División que retorne entero. """

num1 = 2
num2 = 3

print("A.División modular:", (num1), "%", (num2), """ = """, (num1%num2))
print("B. Exponenciación:", (num1), "**", (num2), """ = """, (num1**num2))
print("C. División que retorne entero: ", (num1), "//", (num2), """ = """, (num1//num2))


